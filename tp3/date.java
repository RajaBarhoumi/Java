
public class date {
	 private int year;
	  private int month;
	  private int day;
	  public date(int j,int m,int a) {
		  this.day=j;
		  this.month=m;
		  this.year=a;
	  }
	  public int getDay() {
		  return day;
	  }
	  public int getMonth() {
		  return month;
	  }
	  public int getYear() {
		  return year;
	  }
}
